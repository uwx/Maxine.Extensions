using NFMWorld.LuaSourceGenerator.Test.SampleTypes;

namespace NFMWorld.LuaSourceGenerator.Test;

/// <summary>
/// Integration tests that generate actual code and verify it can be used.
/// </summary>
[TestClass]
public class GeneratedCodeIntegrationTests
{
    private static string _generatedCode = "";

    [ClassInitialize]
    public static void Setup(TestContext context)
    {
        // Generate code once for all tests in this fixture
        var generator = new LuaBindingGenerator();
        var assemblies = new[] { typeof(SampleClass).Assembly };
        _generatedCode = generator.Generate(assemblies);
    }

    [TestMethod]
    public void GeneratedCode_IsNotEmpty()
    {
        Assert.IsFalse(string.IsNullOrEmpty(_generatedCode));
    }

    [TestMethod]
    public void GeneratedCode_HasAutoGeneratedHeader()
    {
        Assert.IsTrue(_generatedCode.StartsWith("// <auto-generated />"));
    }

    [TestMethod]
    public void GeneratedCode_HasRequiredUsings()
    {
        // Base usings are now in LuaBindingsBase.cs, generated code only needs Lua usings
        Assert.IsTrue(_generatedCode.Contains("using LuaNET.LuaJIT;"));
        Assert.IsTrue(_generatedCode.Contains("using static LuaNET.LuaJIT.Lua;"));
    }

    [TestMethod]
    public void GeneratedCode_RegistersAllTestTypes()
    {
        // Should have registration calls for all test types
        Assert.IsTrue(_generatedCode.Contains("Register_NFMWorld_LuaSourceGenerator_Test_SampleTypes_SampleClass"));
        Assert.IsTrue(_generatedCode.Contains("Register_NFMWorld_LuaSourceGenerator_Test_SampleTypes_SampleStruct"));
        Assert.IsTrue(_generatedCode.Contains("Register_NFMWorld_LuaSourceGenerator_Test_SampleTypes_Vector3Struct"));
    }

    [TestMethod]
    public void GeneratedCode_SampleClass_HasCorrectStaticMethods()
    {
        Assert.IsTrue(_generatedCode.Contains("_static_add"));
        Assert.IsTrue(_generatedCode.Contains("_static_concat"));
        Assert.IsTrue(_generatedCode.Contains("_static_incrementCounter"));
    }

    [TestMethod]
    public void GeneratedCode_SampleClass_HasCorrectInstanceMethods()
    {
        Assert.IsTrue(_generatedCode.Contains("_method_getDoubleId"));
        Assert.IsTrue(_generatedCode.Contains("_method_getGreeting"));
        Assert.IsTrue(_generatedCode.Contains("_method_setValue"));
        Assert.IsTrue(_generatedCode.Contains("_method_calculate"));
        Assert.IsTrue(_generatedCode.Contains("_method_clone"));
    }

    [TestMethod]
    public void GeneratedCode_SampleClass_HasCustomNamedMethod()
    {
        Assert.IsTrue(_generatedCode.Contains("case \"customName\":"));
    }

    [TestMethod]
    public void GeneratedCode_SampleClass_DoesNotHaveHiddenMethod()
    {
        // The hidden method should not be exposed
        Assert.IsFalse(_generatedCode.Contains("_method_hiddenMethod"));
        Assert.IsFalse(_generatedCode.Contains("case \"hiddenMethod\":"));
    }

    [TestMethod]
    public void GeneratedCode_Vec2_HasAllOperators()
    {
        // Check for Vec2 (SampleStruct) operators
        Assert.IsTrue(_generatedCode.Contains("__add"));
        Assert.IsTrue(_generatedCode.Contains("__sub"));
        Assert.IsTrue(_generatedCode.Contains("__mul"));
        Assert.IsTrue(_generatedCode.Contains("__div"));
        Assert.IsTrue(_generatedCode.Contains("__unm"));
        Assert.IsTrue(_generatedCode.Contains("__eq"));
    }

    [TestMethod]
    public void GeneratedCode_Vec2_HasStaticProperties()
    {
        Assert.IsTrue(_generatedCode.Contains("case \"zero\":"));
        Assert.IsTrue(_generatedCode.Contains("case \"one\":"));
        Assert.IsTrue(_generatedCode.Contains("case \"unitX\":"));
        Assert.IsTrue(_generatedCode.Contains("case \"unitY\":"));
    }

    [TestMethod]
    public void GeneratedCode_Vec2_HasInstanceProperties()
    {
        Assert.IsTrue(_generatedCode.Contains("case \"length\":"));
        Assert.IsTrue(_generatedCode.Contains("case \"lengthSquared\":"));
    }

    [TestMethod]
    public void GeneratedCode_Vec2_HasFields()
    {
        // X and Y should be exposed as fields in lowercase
        Assert.IsTrue(_generatedCode.Contains("case \"x\":"));
        Assert.IsTrue(_generatedCode.Contains("case \"y\":"));
    }

    [TestMethod]
    public void GeneratedCode_Vec2_HasStaticMethods()
    {
        Assert.IsTrue(_generatedCode.Contains("_static_distance"));
        Assert.IsTrue(_generatedCode.Contains("_static_fromAngle"));
        Assert.IsTrue(_generatedCode.Contains("_static_dot"));
    }

    [TestMethod]
    public void GeneratedCode_Vec2_HasInstanceMethods()
    {
        Assert.IsTrue(_generatedCode.Contains("_method_normalized"));
        Assert.IsTrue(_generatedCode.Contains("_method_scale"));
        Assert.IsTrue(_generatedCode.Contains("_method_set"));
    }

    [TestMethod]
    public void GeneratedCode_Vec3_HasCrossProduct()
    {
        Assert.IsTrue(_generatedCode.Contains("_static_cross"));
    }

    [TestMethod]
    public void GeneratedCode_Vec3_HasCrossTypeMethod()
    {
        // ToVec2 returns a SampleStruct
        Assert.IsTrue(_generatedCode.Contains("_method_toVec2"));

        // FromVec2 takes a SampleStruct parameter
        Assert.IsTrue(_generatedCode.Contains("_static_fromVec2"));
    }

    [TestMethod]
    public void GeneratedCode_UsesUpdateStructForStructMutation()
    {
        // When mutating a struct field or calling a method, UpdateStruct should be called
        Assert.IsTrue(_generatedCode.Contains("UpdateStruct(L, 1, obj)"));
    }

    [TestMethod]
    public void GeneratedCode_UsesGetStructFromStackForStructs()
    {
        Assert.IsTrue(_generatedCode.Contains("GetStructFromStack<NFMWorld.LuaSourceGenerator.Test.SampleTypes.SampleStruct>"));
    }

    [TestMethod]
    public void GeneratedCode_UsesGetObjectFromStackForClasses()
    {
        Assert.IsTrue(_generatedCode.Contains("GetObjectFromStack<NFMWorld.LuaSourceGenerator.Test.SampleTypes.SampleClass>"));
    }

    [TestMethod]
    public void GeneratedCode_KeepsDelegatesAlive()
    {
        // All CFunction delegates should use KeepAlive to prevent GC
        // The _delegates list is in LuaBindingsBase.cs, but KeepAlive calls are in generated code
        Assert.IsTrue(_generatedCode.Contains("KeepAlive("));
        // Storage is now in LuaBindingsBase.cs
        Assert.IsFalse(_generatedCode.Contains("private static readonly List<lua_CFunction> _delegates"));
    }

    [TestMethod]
    public void GeneratedCode_SetsGlobalCorrectly()
    {
        // Types should be set as globals with their Lua names
        Assert.IsTrue(_generatedCode.Contains("lua_setglobal(L, \"SampleClass\")"));
        Assert.IsTrue(_generatedCode.Contains("lua_setglobal(L, \"Vec2\")")); // Custom name
        Assert.IsTrue(_generatedCode.Contains("lua_setglobal(L, \"Vec3\")")); // Custom name
    }

    [TestMethod]
    public void GeneratedCode_HandlesNullableStrings()
    {
        // ToObject is now in LuaBindingsBase.cs, so we just verify generated code uses it
        Assert.IsTrue(_generatedCode.Contains("ToObject(L,"));
    }

    [TestMethod]
    public void GeneratedCode_HasErrorHandling()
    {
        Assert.IsTrue(_generatedCode.Contains("luaL_error"));
    }

    [TestMethod]
    public void GeneratedCode_HasProperMethodDispatch()
    {
        // Methods should check argument count for overload resolution
        Assert.IsTrue(_generatedCode.Contains("var argCount = lua_gettop(L)"));
        Assert.IsTrue(_generatedCode.Contains("if (argCount =="));
    }

    [TestMethod]
    public void GeneratedCode_CanWriteToFile()
    {
        // Verify we can actually write the generated code to a file
        var tempPath = Path.GetTempFileName();
        try
        {
            File.WriteAllText(tempPath, _generatedCode);
            var readBack = File.ReadAllText(tempPath);
            Assert.AreEqual(_generatedCode, readBack);
        }
        finally
        {
            if (File.Exists(tempPath))
            {
                File.Delete(tempPath);
            }
        }
    }
}
